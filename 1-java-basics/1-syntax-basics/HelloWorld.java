/**
 * Java Syntax Basics - HelloWorld.java
 * 
 * This is your first Java program.
 * It demonstrates:
 * - How to create a Java class
 * - How to write the main method
 * - How to print output
 * 
 * To run:
 *   javac HelloWorld.java
 *   java HelloWorld
 */

public class HelloWorld {
    // The main method - this is where Java programs start
    public static void main(String[] args) {
        // Print hello world to console
        System.out.println("Hello, World!");
        
        // Print multiple lines
        System.out.println("Welcome to Java!");
        System.out.println("Let's learn programming!");
    }
}

/* 
 * OUTPUT:
 * Hello, World!
 * Welcome to Java!
 * Let's learn programming!
 */
